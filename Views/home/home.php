    <section class='how-to-use'>
        <h3 id='how-to-title'>2. Téléchargement</h3>
        <section>
            <p>Une fois connecté, vous pouvez accéder à la page de téléchargement en cliquant sur le bouton dans la barre de navigation.
            Si vous êtes sur smartphone, vous ne pourrez pas lancer le jeu mais vous pouvez tout de même le télécharger
             afin de le transférer ultérieurement sur votre pc.</p>
        </section>
    </section>
    <section class='how-to-use'>
        <h3 id='how-to-title'>3. Java</h3>
        <section>
            <p>Pour lancer l'application, vous devez nécessairement avoir Java 17 ou une version ultérieure d'installée.
             Le JDK doit contenir obligatoirement JavaFx pour que l'application s'exécute, téléchargez la version pour votre ordinateur
            ci-dessous !</p>
        </section>
